class FavouritPage extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    var theme = Theme.of(context);
    var appState = context.watch<MyAppState>();

    if (appState.messages.isEmpty) {
      return Center(
        child: Text('There are no favorites selected yet!'),
      );
    }

    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Padding(
          padding: const EdgeInsets.all(30),
          child: Text('You have '
              '${appState.messages.length} favorites:'),
        ),
        Expanded(
          child: ListView.separated(
              itemCount: appState.messages.length,
              itemBuilder: (context, index) {
                return Dismissible(
                  key: Key(appState.messages[index]),
                  onDismissed: (direction) {
                    // Step 2
                    appState.removeFavorite(appState.messages[index]);
                    ScaffoldMessenger.of(context).showSnackBar(SnackBar(
                        content:
                            Text('${appState.messages[index]} dismissed')));
                  },
                  child: ListTile(
                    leading: CircleAvatar(
                      radius: 15,
                      backgroundColor: theme.colorScheme.primary,
                      child: Text((index + 1).toString(),
                          style: TextStyle(color: theme.colorScheme.onPrimary)),
                    ),
                    title: Text(
                      '${appState.messages[index]}',
                    ),
                    //subtitle: Text('Item description'),
                    trailing: IconButton(
                        icon:
                            Icon(Icons.delete_outline, semanticLabel: 'Delete'),
                        color: theme.colorScheme.primary,
                        onPressed: () {
                          appState.removeFavorite(appState.messages[index]);
                          ScaffoldMessenger.of(context).showSnackBar(SnackBar(
                              content:
                                  Text('${appState.messages[index]} deleted')));
                        }),
                  ),
                );
              },
              separatorBuilder: (context, index) {
                return Divider();
              }),
        ),
      ],
    );
  }
}

